-- Basic setup
USE ROLE SYSADMIN;
CREATE DATABASE IF NOT EXISTS ML_DEMO_DB;
CREATE SCHEMA IF NOT EXISTS ML_DEMO_SCHEMA;

-- Set up compute pool
CREATE OR REPLACE COMPUTE POOL ML_DEMO_POOL
    MIN_NODES = 1
    MAX_NODES = 10
    INSTANCE_FAMILY = HIGHMEM_X64_S;

-- Start compute pool if necessary
SHOW COMPUTE POOLS LIKE 'ML_DEMO_POOL';
ALTER COMPUTE POOL ML_DEMO_POOL RESUME;