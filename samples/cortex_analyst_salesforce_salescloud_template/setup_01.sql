-- Set context for role and warehouse
create database if not exists SFDC;
create schema if not exists SALESCLOUD;
create stage if not exists SEMANTICS;

--Create SFDC Sample Tables
create or replace TABLE USER (
	ID VARCHAR(18),
	DEPARTMENT VARCHAR(80),
	USERNAME VARCHAR(80),
	FIRSTNAME VARCHAR(40),
	TITLE VARCHAR(80),
	LASTNAME VARCHAR(80),
	NAME VARCHAR(121),
	SYSTEMMODSTAMP TIMESTAMP_NTZ(9)
);
create or replace TABLE ACCOUNT (
	ID VARCHAR(18),
	LASTMODIFIEDDATE TIMESTAMP_NTZ(9),
	NAME VARCHAR(255),
	INDUSTRY VARCHAR(255),
	ACCOUNTSOURCE VARCHAR(255),
	WEBSITE VARCHAR(255),
	OWNERID VARCHAR(18),
	ANNUALREVENUE NUMBER(18,0),
	BILLINGCITY VARCHAR(40),
	BILLINGSTATE VARCHAR(80),
	BILLINGCOUNTRY VARCHAR(80)
);
create or replace TABLE OPPORTUNITY (
	ID VARCHAR(18),
	NAME VARCHAR(120),
	FORECASTCATEGORY VARCHAR(40),
	STAGENAME VARCHAR(255),
	ISCLOSED BOOLEAN,
	ISWON BOOLEAN,
	CREATEDDATE TIMESTAMP_NTZ(9),
	CLOSEDATE DATE,
	PROBABILITY NUMBER(3,0),
	LEADSOURCE VARCHAR(255),
	AMOUNT NUMBER(18,2),
	FISCALQUARTER NUMBER(8,0),
	FISCALYEAR NUMBER(8,0),
	ACCOUNTID VARCHAR(16777216),
    OWNERID VARCHAR(255)
);
