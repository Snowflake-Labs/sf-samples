-- ============================================================================
-- Martech: 01_infrastructure.sql
-- Provision database, schemas, warehouse, role, grants. Parameterized.
-- Default deployment_db = MARTECH; override via:
--   snow sql -f deploy/01_infrastructure.sql -D deployment_db=MY_DB
-- ============================================================================

USE ROLE ACCOUNTADMIN;

-- IDR Engine database (shared engine procedures)
CREATE DATABASE IF NOT EXISTS IDR;
CREATE SCHEMA IF NOT EXISTS IDR.PROCEDURES;
CREATE SCHEMA IF NOT EXISTS IDR.CONFIG;

-- Use-case database
CREATE DATABASE IF NOT EXISTS IDENTIFIER('&{deployment_db}');

-- Schemas
CREATE SCHEMA IF NOT EXISTS IDENTIFIER('&{deployment_db}.BRONZE');
CREATE SCHEMA IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER');
CREATE SCHEMA IF NOT EXISTS IDENTIFIER('&{deployment_db}.GOLD');
CREATE SCHEMA IF NOT EXISTS IDENTIFIER('&{deployment_db}.APP');
CREATE SCHEMA IF NOT EXISTS IDENTIFIER('&{deployment_db}.CONFIG');

-- Internal stage for synthetic-data loading (PUT/COPY by martech_data_generator).
-- COPY supplies an inline CSV file format, so no default format is needed here.
CREATE STAGE IF NOT EXISTS IDENTIFIER('&{deployment_db}.BRONZE.SAMPLE_DATA_STAGE');

-- Tags (engine convention)
CREATE TAG IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.IDR_INCLUDE');
CREATE TAG IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.IDR_PRIMARY_KEY');
CREATE TAG IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.IDR_PRIORITY');
CREATE TAG IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.IDR_SOURCE_ROLE');
CREATE TAG IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.IDR_IDENTIFIER');

-- Warehouse
CREATE WAREHOUSE IF NOT EXISTS MARTECH_WH
    WAREHOUSE_SIZE = 'X-SMALL'
    AUTO_SUSPEND = 60
    AUTO_RESUME = TRUE
    INITIALLY_SUSPENDED = TRUE;

-- App role + grants
CREATE ROLE IF NOT EXISTS MARTECH_APP_ROLE;
GRANT USAGE ON WAREHOUSE MARTECH_WH TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON ALL SCHEMAS IN DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;
GRANT SELECT, INSERT, UPDATE, DELETE ON FUTURE TABLES IN DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;
GRANT SELECT ON FUTURE VIEWS IN DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON FUTURE FUNCTIONS IN DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;
GRANT EXECUTE TASK ON ACCOUNT TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON FUTURE PROCEDURES IN DATABASE IDENTIFIER('&{deployment_db}') TO ROLE MARTECH_APP_ROLE;

-- App role grants on shared IDR engine database
GRANT USAGE ON DATABASE IDR TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON SCHEMA IDR.CONFIG TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON SCHEMA IDR.PROCEDURES TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON ALL PROCEDURES IN SCHEMA IDR.PROCEDURES TO ROLE MARTECH_APP_ROLE;
GRANT USAGE ON FUTURE PROCEDURES IN SCHEMA IDR.PROCEDURES TO ROLE MARTECH_APP_ROLE;

-- Image repository (for SPCS deployment)
CREATE IMAGE REPOSITORY IF NOT EXISTS IDENTIFIER('&{deployment_db}.APP.IMAGES');

-- Compute pool (for SPCS deployment)
CREATE COMPUTE POOL IF NOT EXISTS MARTECH_POOL
    MIN_NODES = 1
    MAX_NODES = 1
    INSTANCE_FAMILY = CPU_X64_XS
    AUTO_SUSPEND_SECS = 3600
    AUTO_RESUME = TRUE;
