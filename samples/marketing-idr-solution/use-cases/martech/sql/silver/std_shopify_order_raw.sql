-- ============================================================================
-- Martech: SILVER.STD_SHOPIFY_ORDER_RAW
-- Flattened Shopify Order JSON into typed identifier columns.
-- ============================================================================

CREATE TABLE IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.STD_SHOPIFY_ORDER_RAW') (
    -- Original bronze columns
    ORDER_ID         VARCHAR,
    ORDER_NUMBER     NUMBER,
    CREATED_AT_SRC   TIMESTAMP_NTZ,
    TOTAL_PRICE      NUMBER(12,2),
    FINANCIAL_STATUS VARCHAR,
    RAW_PAYLOAD      VARIANT,
    SOURCE_FILE      VARCHAR,
    INGESTED_AT      TIMESTAMP_NTZ,
    -- Flattened identifier columns from Shopify JSON
    EMAIL                  VARCHAR,
    EMAIL_STD              VARCHAR,
    EMAIL_HEM              VARCHAR,
    PHONE                  VARCHAR,
    PHONE_E164             VARCHAR,
    SHOPIFY_CUSTOMER_ID    VARCHAR,
    LOYALTY_MEMBER_NUMBER  VARCHAR,
    LOYALTY_MEMBER_NUMBER_STD VARCHAR,
    FIRST_NAME             VARCHAR,
    LAST_NAME              VARCHAR,
    FIRST_NAME_STD         VARCHAR,
    LAST_NAME_STD          VARCHAR,
    FIRST_NAME_CANONICAL   VARCHAR,
    BILLING_STREET_ADDRESS VARCHAR,
    BILLING_CITY           VARCHAR,
    BILLING_STATE          VARCHAR,
    BILLING_POSTAL_CODE    VARCHAR,
    BILLING_COUNTRY        VARCHAR,
    SHIPPING_STREET_ADDRESS VARCHAR,
    SHIPPING_CITY          VARCHAR,
    SHIPPING_STATE         VARCHAR,
    SHIPPING_POSTAL_CODE   VARCHAR,
    SHIPPING_COUNTRY       VARCHAR,
    BROWSER_IP             VARCHAR,
    USER_AGENT             VARCHAR,
    SESSION_HASH           VARCHAR,
    GCLID                  VARCHAR,
    FBCLID                 VARCHAR,
    TTCLID                 VARCHAR,
    LANDING_SITE           VARCHAR,
    -- v1: ML blocking attributes
    EMAIL_HANDLE_NORMALIZED  VARCHAR,
    SOUNDEX_LNAME            VARCHAR,
    PHONE_LAST10             VARCHAR,
    PHONE_LAST7              VARCHAR,
    PHONE_AREA_CODE          VARCHAR,
    IS_BLOCKABLE_EMAIL       BOOLEAN,
    IS_BLOCKABLE_PHONE       BOOLEAN,
    ML_BLOCKING_PROCESSED    BOOLEAN DEFAULT FALSE,
    ML_BLOCKING_PROCESSED_AT TIMESTAMP_NTZ,
    -- Processing metadata
    STD_PROCESSED_AT  TIMESTAMP_NTZ,
    STD_BATCH_ID      VARCHAR,
    IDR_PROCESSED     BOOLEAN DEFAULT FALSE,
    IDR_PROCESSED_AT  TIMESTAMP_NTZ,
    METADATA_ACTION   VARCHAR DEFAULT 'INSERT',
    METADATA_ISUPDATE BOOLEAN DEFAULT FALSE
);

