-- ============================================================================
-- Martech: SILVER.STD_POS_TRANSACTION_RAW
-- Output of SP_CUSTOM_STANDARDIZE consuming POS_TRANSACTION_STREAM.
-- Original columns preserved + *_STD canonical forms + IDR processing flags.
-- ============================================================================

CREATE TABLE IF NOT EXISTS IDENTIFIER('&{deployment_db}.SILVER.STD_POS_TRANSACTION_RAW') (
    -- Original bronze columns
    TXN_ID                VARCHAR,
    STORE_ID              VARCHAR,
    TS                    TIMESTAMP_NTZ,
    TENDER_TYPE           VARCHAR,
    TOTAL                 NUMBER(12,2),
    LOYALTY_MEMBER_NUMBER VARCHAR,
    CUSTOMER_EMAIL        VARCHAR,
    CUSTOMER_PHONE        VARCHAR,
    CUSTOMER_FIRST_NAME   VARCHAR,
    CUSTOMER_LAST_NAME    VARCHAR,
    LINE_ITEMS_JSON       VARIANT,
    SOURCE_FILE           VARCHAR,
    INGESTED_AT           TIMESTAMP_NTZ,
    -- Standardized identifier columns
    EMAIL_STD             VARCHAR,
    EMAIL_HEM             VARCHAR,
    PHONE_E164            VARCHAR,
    LOYALTY_MEMBER_NUMBER_STD VARCHAR,
    FIRST_NAME_STD        VARCHAR,
    LAST_NAME_STD         VARCHAR,
    FIRST_NAME_CANONICAL  VARCHAR,
    -- Prefixed *_STD shadows: the engine identifier extractor probes
    -- <bronze_col>_STD (CUSTOMER_EMAIL_STD, ...). SP_CUSTOM_STANDARDIZE writes them.
    CUSTOMER_EMAIL_STD        VARCHAR,
    CUSTOMER_PHONE_STD        VARCHAR,
    CUSTOMER_FIRST_NAME_STD   VARCHAR,
    CUSTOMER_LAST_NAME_STD    VARCHAR,
    -- v1: ML blocking attributes (computed in SP_CUSTOM_STANDARDIZE)
    EMAIL_HANDLE_NORMALIZED  VARCHAR,
    SOUNDEX_LNAME            VARCHAR,
    PHONE_LAST10             VARCHAR,
    PHONE_LAST7              VARCHAR,
    PHONE_AREA_CODE          VARCHAR,
    IS_BLOCKABLE_EMAIL       BOOLEAN,
    IS_BLOCKABLE_PHONE       BOOLEAN,
    ML_BLOCKING_PROCESSED    BOOLEAN DEFAULT FALSE,
    ML_BLOCKING_PROCESSED_AT TIMESTAMP_NTZ,
    -- Processing metadata
    STD_PROCESSED_AT      TIMESTAMP_NTZ,
    STD_BATCH_ID          VARCHAR,
    IDR_PROCESSED         BOOLEAN DEFAULT FALSE,
    IDR_PROCESSED_AT      TIMESTAMP_NTZ,
    METADATA_ACTION       VARCHAR DEFAULT 'INSERT',
    METADATA_ISUPDATE     BOOLEAN DEFAULT FALSE
);

