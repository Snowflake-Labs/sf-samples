-- ============================================================================
-- AI Blocking Configuration Tables
-- Metadata-driven configuration for AI second pass candidate generation
-- ============================================================================

USE ROLE ACCOUNTADMIN;
USE DATABASE IDR;
USE SCHEMA CONFIG;
USE WAREHOUSE IDR_DEMO_WH;

-- Blocking strategy configuration table
CREATE TABLE IF NOT EXISTS CONFIG.IDR_ML_AI_BLOCKING_CONFIG (
    BLOCKING_TIER       INT NOT NULL,
    BLOCKING_NAME       VARCHAR(100) NOT NULL,
    BLOCKING_KEY_COLUMN VARCHAR(100) NOT NULL,   -- Column name in IDR_CORE_CLUSTER
    WEIGHT              INT NOT NULL DEFAULT 50,
    IS_ENABLED          BOOLEAN NOT NULL DEFAULT TRUE,
    MIN_CLUSTER_SIZE    INT DEFAULT 1,
    DESCRIPTION         VARCHAR(500),
    CREATED_AT          TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    UPDATED_AT          TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (BLOCKING_TIER)
);

-- Seed the blocking configuration
MERGE INTO CONFIG.IDR_ML_AI_BLOCKING_CONFIG tgt
USING (
    SELECT * FROM VALUES
        (1, 'EMAIL_DOMAIN_LOCAL_SOUNDEX', 'BLOCK_DOMAIN_SOUNDEX',     80, TRUE,  1, 'Email domain + soundex of local part - high confidence similar emails'),
        (2, 'PHONE_LAST7_LNAME_SOUNDEX',  'BLOCK_PHONE7_SOUNDEX',     60, TRUE,  1, 'Last 7 digits of phone + last name soundex'),
        (3, 'DOMAIN_LNAME_SOUNDEX',       'BLOCK_DOMAIN_SOUNDEX',     50, TRUE,  1, 'Email domain + last name soundex'),
        (4, 'ZIP3_LNAME_SOUNDEX_FI',      'BLOCK_ZIP3_SOUNDEX_FI',    40, TRUE,  1, 'First 3 digits of ZIP + last name soundex + first initial'),
        (5, 'DOB_MMDD_LNAME_SOUNDEX',     'BLOCK_DOB_MMDD_SOUNDEX',   40, FALSE, 1, 'Birth month/day + last name soundex')
    AS src(BLOCKING_TIER, BLOCKING_NAME, BLOCKING_KEY_COLUMN, WEIGHT, IS_ENABLED, MIN_CLUSTER_SIZE, DESCRIPTION)
) src
ON tgt.BLOCKING_TIER = src.BLOCKING_TIER
WHEN MATCHED THEN UPDATE SET
    BLOCKING_NAME = src.BLOCKING_NAME,
    BLOCKING_KEY_COLUMN = src.BLOCKING_KEY_COLUMN,
    WEIGHT = src.WEIGHT,
    IS_ENABLED = src.IS_ENABLED,
    MIN_CLUSTER_SIZE = src.MIN_CLUSTER_SIZE,
    DESCRIPTION = src.DESCRIPTION,
    UPDATED_AT = CURRENT_TIMESTAMP()
WHEN NOT MATCHED THEN INSERT 
    (BLOCKING_TIER, BLOCKING_NAME, BLOCKING_KEY_COLUMN, WEIGHT, IS_ENABLED, MIN_CLUSTER_SIZE, DESCRIPTION)
VALUES 
    (src.BLOCKING_TIER, src.BLOCKING_NAME, src.BLOCKING_KEY_COLUMN, src.WEIGHT, src.IS_ENABLED, src.MIN_CLUSTER_SIZE, src.DESCRIPTION);

-- AI Candidate Pairs table - stores candidate pairs for AI evaluation
CREATE TABLE IF NOT EXISTS CONFIG.IDR_ML_AI_CANDIDATE_PAIRS (
    CANDIDATE_ID        VARCHAR(50) NOT NULL,
    CLUSTER_ID_1        VARCHAR(50) NOT NULL,
    CLUSTER_ID_2        VARCHAR(50) NOT NULL,
    BLOCKING_TIER       INT NOT NULL,
    BLOCKING_NAME       VARCHAR(100),
    BLOCKING_KEY_VALUE  VARCHAR(500),
    WEIGHT              INT,
    STATUS              VARCHAR(20) DEFAULT 'PENDING',  -- PENDING, EVALUATING, MATCH, NO_MATCH, REVIEW
    AI_SCORE            FLOAT,
    AI_REASONING        VARCHAR(5000),
    AI_EVALUATED_AT     TIMESTAMP_NTZ,
    REVIEWED_BY         VARCHAR(100),
    REVIEWED_AT         TIMESTAMP_NTZ,
    REVIEW_DECISION     VARCHAR(20),  -- APPROVED, REJECTED
    CREATED_AT          TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (CANDIDATE_ID)
);


-- Stoplist table for blocking keys that are too common (generate too many pairs)
CREATE TABLE IF NOT EXISTS CONFIG.AI_BLOCKING_STOPLIST (
    BLOCKING_NAME       VARCHAR(100) NOT NULL,
    BLOCKING_KEY_VALUE  VARCHAR(500) NOT NULL,
    FREQUENCY           INT,
    REASON              VARCHAR(200),
    CREATED_AT          TIMESTAMP_NTZ DEFAULT CURRENT_TIMESTAMP(),
    PRIMARY KEY (BLOCKING_NAME, BLOCKING_KEY_VALUE)
);

-- View to see current blocking configuration
CREATE OR REPLACE VIEW CONFIG.V_AI_BLOCKING_CONFIG AS
SELECT 
    BLOCKING_TIER,
    BLOCKING_NAME,
    BLOCKING_KEY_COLUMN,
    WEIGHT,
    IS_ENABLED,
    MIN_CLUSTER_SIZE,
    DESCRIPTION,
    UPDATED_AT
FROM CONFIG.IDR_ML_AI_BLOCKING_CONFIG
ORDER BY BLOCKING_TIER;

SELECT 'AI Blocking Configuration tables created successfully' AS STATUS;
SELECT * FROM CONFIG.V_AI_BLOCKING_CONFIG;
