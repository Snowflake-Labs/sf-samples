USE ROLE <% ctx.env.finops_db_admin_role%>;
USE DATABASE <% ctx.env.finops_acct_db %>;
USE SCHEMA <% ctx.env.finops_acct_schema %>;
CREATE OR REPLACE VIEW TABLE_RECREATION_SCHEMA_TABLETYPE_COUNT COMMENT = 'Title: Table Recreation Counts by Schema and Table Type. Description: Analyze tables that are recreated by schema.'
AS
WITH MAX_DATE
AS
   (
   SELECT MAX(DATE_COLLECTED) AS LATEST_SNAPSHOT
   FROM TABLE_STORAGE_DETAILED
   )
SELECT DISTINCT COALESCE(TS.TAG_VALUE, TABLE_SCHEMA) COST_CENTER,
   TABLE_CATALOG || '.' || TABLE_SCHEMA AS SCHEMA_NAME,
   CASE WHEN IS_TRANSIENT = 'YES' THEN 'TRANSIENT' ELSE 'STANDARD' END TABLE_TYPE,
   TABLE_COPY_COUNT - 1 AS TABLE_RECREATED_COUNT
FROM TABLE_STORAGE_DETAILED tsd  -- Generated table storage detailed metrics
LEFT JOIN SNOWFLAKE.ACCOUNT_USAGE.TAG_REFERENCES TS ON TS.OBJECT_DATABASE = TSD.TABLE_CATALOG
   AND TS.OBJECT_NAME = TSD.TABLE_SCHEMA
   AND TS.TAG_NAME = '<% ctx.env.finops_tag_name %>'
   AND TS.DOMAIN = 'SCHEMA'
WHERE DATE_COLLECTED = (
       SELECT LATEST_SNAPSHOT
       FROM MAX_DATE
       )
GROUP BY ALL;
